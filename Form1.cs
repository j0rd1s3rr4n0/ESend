            }
        }

        public static bool IsSMTPOpen(string server)
        {
            int[] smtpPorts = { 25, 587, 465 };
            foreach (int port in smtpPorts)
            {
                if (IsPortOpen(server, port))
                {
                    return false;
                }
            }
            return true;
        }


